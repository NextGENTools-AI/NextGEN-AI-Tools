export function getToolBySlug(slug: string): Tool | undefined {
  return tools.find((t) => t.slug === slug);
}

export function getToolById(id: string): Tool | undefined {
  return tools.find((t) => t.id === id);
}

export function getAllTools(): Tool[] {
  return tools;
}

export function getCategoryBySlug(slug: string): Category | undefined {
  return categories.find((category) => category.slug === slug);
}

export function getToolsByCategory(categorySlug: string): Tool[] {
  return tools.filter((t) => t.categorySlug === categorySlug);
}

export function getFeaturedTools(limit = 6): Tool[] {
  return tools.slice(0, limit);
}

export function getTopRatedTools(limit = 10): Tool[] {
  return [...tools]
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit);
}

export function getRelatedTools(currentSlug: string, limit = 4): Tool[] {
  const current = getToolBySlug(currentSlug);
  if (!current) return [];

  return tools
    .filter((t) => t.slug !== currentSlug)
    .filter((t) => t.categorySlug === current.categorySlug)
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit);
}

export function getToolsByPricing(pricing: string): Tool[] {
  return tools.filter(
    (t) => t.pricing.toLowerCase() === pricing.toLowerCase()
  );
}

export function getFreeTools(): Tool[] {
  return tools.filter(
    (t) =>
      t.pricing.toLowerCase() === 'free' ||
      t.pricing.toLowerCase() === 'freemium'
  );
}


export function searchTools(query: string): Tool[] {
  const q = query.toLowerCase().trim();
  if (!q) return tools;

  return tools.filter(
    (t) =>
      t.name.toLowerCase().includes(q) ||
      t.tagline.toLowerCase().includes(q) ||
      t.description.toLowerCase().includes(q) ||
      t.category.toLowerCase().includes(q) ||
      t.features.some((f) => f.toLowerCase().includes(q)) ||
      t.useCases.some((u) => u.toLowerCase().includes(q))
  );
}

export function getAllCategories(): { name: string; slug: string }[] {
  const seen = new Set<string>();
  const categories: { name: string; slug: string }[] = [];

  tools.forEach((t) => {
    if (!seen.has(t.categorySlug)) {
      seen.add(t.categorySlug);
      categories.push({ name: t.category, slug: t.categorySlug });
    }
  });

  return categories.sort((a, b) => a.name.localeCompare(b.name));
}

export function getToolCountByCategory(): Record<string, number> {
  return tools.reduce<Record<string, number>>((acc, t) => {
    acc[t.categorySlug] = (acc[t.categorySlug] ?? 0) + 1;
    return acc;
  }, {});
}

export function getAlternativeTools(toolName: string, limit = 4): Tool[] {
  return tools
    .filter((t) =>
      t.alternatives?.some(
        (alt) => alt.toLowerCase() === toolName.toLowerCase()
      )
    )
    .slice(0, limit);
}

export function getRecentlyUpdatedTools(limit = 5): Tool[] {
  return [...tools]
    .filter((t) => t.updatedAt !== undefined)
    .sort(
      (a, b) =>
        new Date(b.updatedAt!).getTime() - new Date(a.updatedAt!).getTime()
    )
    .slice(0, limit);
}

export function getToolsByRatingRange(min: number, max: number): Tool[] {
  return tools.filter((t) => t.rating >= min && t.rating <= max);
}

