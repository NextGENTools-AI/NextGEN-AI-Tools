function parseReviewCount(value: string): number {
  const normalized = value.toLowerCase();
  const numeric = Number.parseFloat(normalized.replace(/k/g, ''));
  if (normalized.includes('k')) return numeric * 1000;
  return numeric;
}

function sortToolsByPopularity(a: Tool, b: Tool): number {
  const aBoost = Object.entries(popularityBoosts).reduce((score, [key, weight]) => {
    return score + (a.name.toLowerCase().includes(key) || a.tagline.toLowerCase().includes(key) ? weight : 0);
  }, 0);
  const bBoost = Object.entries(popularityBoosts).reduce((score, [key, weight]) => {
    return score + (b.name.toLowerCase().includes(key) || b.tagline.toLowerCase().includes(key) ? weight : 0);
  }, 0);

  if (bBoost !== aBoost) return bBoost - aBoost;
  if (b.rating !== a.rating) return b.rating - a.rating;
  return parseReviewCount(b.reviews) - parseReviewCount(a.reviews);
}

